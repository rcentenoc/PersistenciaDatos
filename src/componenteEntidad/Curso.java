
package componenteEntidad;

import java.util.Objects;

/**
 *
 * @author Fab
 */
public class Curso {
    
    private String idCurso;
    private Profesor profesor;
    private String nombre;

    public Curso(){
    
    }
    public Curso(String idCurso, Profesor profesor,String nombre) {
        this.idCurso = idCurso;
        this.profesor = profesor;
        this.nombre = nombre;
    }

    public String getIdCurso() {
        return idCurso;
    }

    public void setIdCurso(String idCurso) {
        this.idCurso = idCurso;
    }

    public Profesor getProfesor() {
        return profesor;
    }

    public void setProfesor(Profesor profesor) {
        this.profesor = profesor;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    @Override
    public int hashCode() {
        int hash = 3;
        hash = 53 * hash + Objects.hashCode(this.idCurso);
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Curso other = (Curso) obj;
        if (!Objects.equals(this.idCurso, other.idCurso)) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return idCurso + profesor + nombre ;
    }
    
    
}
