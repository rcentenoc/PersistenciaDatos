    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            String idProfesor = (String) cboIdprofesor.getSelectedItem();
            ArrayList<Curso> lista = CursoCN.getInstancia().mostrarCursosProfesor(idProfesor);
            int n=modelo.getRowCount();
            for(int i=0;i<n;i++)
            modelo.removeRow(0);
            for(int i=0;i<lista.size();i++)
            {
                Curso curso=lista.get(i);
                Object[] fila = {curso.getIdCurso(), curso.getNombre()};
                txtNombre.setText(curso.getProfesor().getNombres());
                modelo.addRow(fila);
            }
        }catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

        
    }//GEN-LAST:event_btnBuscarActionPerformed

