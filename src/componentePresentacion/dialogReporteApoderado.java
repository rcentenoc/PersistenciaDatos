    DefaultTableModel modelo = new DefaultTableModel();
    String nombresColumnas[] = {"idApoderado","Nombres","Apellidos","Telefono","Direcci√≥n","Estado Civil"};
    public dialogReporteApoderado() {
        super(FrmPrincipal.getInstancia(), true);
        initComponents();
        
        setLocationRelativeTo(null);
        modelo.setColumnIdentifiers(nombresColumnas);
        try{
            ArrayList<Apoderado> lista = ApoderadoCN.getInstancia().mostrarApoderados();
            Object datos[][]= new Object[lista.size()][6];
            for(int i=0; i<lista.size();i++){
                Apoderado apoderado = lista.get(i);
                datos[i][0] = apoderado.getIdApoderado();
                datos[i][1] = apoderado.getNombres();
                datos[i][2] = apoderado.getApellidos();
                datos[i][3] = apoderado.getTelefono();
                datos[i][4] = apoderado.getDireccion();
                datos[i][5] = apoderado.getEstadoCivil();
            }
            modelo.setDataVector(datos, nombresColumnas);
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
        try{
            lblNumeroAlumnos.setText(""+ApoderadoCN.getInstancia().numeroApoderados());
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

