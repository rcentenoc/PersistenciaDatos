    public void mostrarTablas(){
         modelo.setColumnIdentifiers(nombreColumnas);
        
        try{
            ArrayList<Horario> lista = HorarioCN.getInstancia().mostrarHorarios();
            Object datos[][] = new Object[lista.size()][6];
            for(int i=0;i< lista.size();i++){
                Horario horario = lista.get(i);
                datos[i][0] = horario.getIdHorario();
                datos[i][1] = horario.getGrado();
                datos[i][2] = horario.getTurno();
                datos[i][3] = horario.getFecha();
                datos[i][4] = horario.getHoraInicio();
                datos[i][5] = horario.getHoraSalida();
            }
            modelo.setDataVector(datos, nombreColumnas);
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
